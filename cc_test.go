func (s *MySuite) TestDeserialize(c *C) {
	type Test struct {
		F0   uint8
		F1   uint16 `endianness:"be"`
		F2   uint16
		F3   uint32
		F4   [8]byte
		F5   [16]byte
		F6   [18]byte
		F7   [32]byte
		F8   [42]byte
		F9   [100]byte
		F10  string `subtype:"longaddr"` // string '0x00124b00019c2ee9'
		F011 uint8
		F11  []uint16
		F012 uint8
		F12  []byte
		F013 uint8
		F13  []*Network
	}
	networks := []*Network{
		&Network{
			NeighborPanID:   400,
			LogicalChannel:  5,
			StackProfile:    4,
			ZigbeeVersion:   5,
			BeaconOrder:     6,
			SuperFrameOrder: 7,
			PermitJoin:      200,
		},
		&Network{
			NeighborPanID:   500,
			LogicalChannel:  2,
			StackProfile:    3,
			ZigbeeVersion:   4,
			BeaconOrder:     5,
			SuperFrameOrder: 6,
			PermitJoin:      100,
		},
	}
	test := &Test{F0: 1, F1: 2, F2: 2, F3: 3, F10: "0x00124b00019c2ee9",
		F011: 2, F11: []uint16{4, 5}, F012: 3, F12: []byte{1, 2, 3},
		F013: 2, F13: networks}
	payload := serialize(test)
	res := &Test{}
	deserialize(payload, res)
	c.Assert(res, DeepEquals, test)
}
